package com.application.timeplans;

public class Approval {

	
	private int approvalState;
	private int person_in_mgt_id;
	private String date;
	
	
	
	public int getApprovalState() {
		return approvalState;
	}
	public void setApprovalState(int approvalState) {
		this.approvalState = approvalState;
	}
	public int getPerson_in_mgt_id() {
		return person_in_mgt_id;
	}
	public void setPerson_in_mgt_id(int person_in_mgt_id) {
		this.person_in_mgt_id = person_in_mgt_id;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	
	
	
}
